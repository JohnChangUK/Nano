package com.nano.client;

public class History {

    public History() {
    }

    private String hash;
    private String type;
    private String account;
    private String amount;

    public String getHash() {
        return hash;
    }

    public String getType() {
        return type;
    }

    public String getAccount() {
        return account;
    }

    public String getAmount() {
        return amount;
    }
}
